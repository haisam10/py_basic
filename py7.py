# 1. Arithmetic operators
print('========= Arithmetic operators ==========')
x = 10
y = 20

# Addition +
print(x + y)

# Subtraction -
print(x - y)

# Multiplication *
print(x * y)

# Division /
print(x / y)

# Modulus %
print(x % y)

# Exponentiation **
a = 5
x = 2
print(a ** x) # (5 X 5) =25 ;( 5 ^ 2 to the power )

# Floor division //
x = 15
