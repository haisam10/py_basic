# int type data
print('========int type data========')
x = 1234
print(type(x))
print("\n")

# float type data
print('========float type data========')
x = 1.2
print(type(x))
print("\n")

# complex type data
print('========complex type data========')
x = 1234j  # if you can't write ' j ' the data show error
print(type(x))
print("\n")

# str type data
print('========str type data========')
Name = "admin"
print(type(Name))
print("\n")

# boolean Type data
print('========boolean type data========')
x = True
y: bool = False
print(x, y)
print(type(x))
print("\n")

# boolean Type data count
print('========boolean type data count========')
x = 10
