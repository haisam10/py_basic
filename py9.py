# 3. Comparison operators
'''
== Equal
!= Not equal
> 	Greater than
< 	Less than
>= Greater than or equal to
<= Less than or equal to
'''

x = 5
y = 10

print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)